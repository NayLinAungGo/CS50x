#include <cs50.h>
#include <stdio.h>

void print_row(int space, int bricks);

int main(void)
{
	int n;
	do
	{
		n = get_int("Height: ");
	}
	while (n < 0);

	for (int i = 1; i <= n; i++)
	{
		print_row(n - i, i);
	}
}

void print_row(int space, int bricks)
{
	for (int i = 0; i < space; i++)
	{
		printf(" ");
	}
	for (int i = 0; i < bricks; i++)
	{
		printf("#");
	}
	printf("\n");
}
